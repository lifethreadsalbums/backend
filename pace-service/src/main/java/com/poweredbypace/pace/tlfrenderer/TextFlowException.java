package com.poweredbypace.pace.tlfrenderer;

public class TextFlowException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1563746277295728812L;

	public TextFlowException() {
		super();
	}

	public TextFlowException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TextFlowException(String arg0) {
		super(arg0);
	}

	public TextFlowException(Throwable arg0) {
		super(arg0);
	}

}
