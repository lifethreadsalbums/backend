package com.poweredbypace.pace.exception;

public class PrintGenerationException extends Exception {

	private static final long serialVersionUID = 9187849895599644409L;

	public PrintGenerationException() {
	}

	public PrintGenerationException(String arg0) {
		super(arg0);
	}

	public PrintGenerationException(Throwable arg0) {
		super(arg0);
	}

	public PrintGenerationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}