package com.poweredbypace.pace.exception;

public class ImageProcessingException extends RuntimeException {

	private static final long serialVersionUID = -744896550733670265L;
	
	public ImageProcessingException() { super(); }
	public ImageProcessingException(String arg0) { super(arg0); }
	public ImageProcessingException(Throwable arg0) { super(arg0); }
	public ImageProcessingException(String arg0, Throwable arg1) { super(arg0, arg1); }

}