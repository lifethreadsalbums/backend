package com.poweredbypace.pace.exception;

public class JpegGenerationException extends RuntimeException {

	private static final long serialVersionUID = 7850023304939823795L;

	public JpegGenerationException() {
	}

	public JpegGenerationException(String arg0) {
		super(arg0);
	}

	public JpegGenerationException(Throwable arg0) {
		super(arg0);
	}

	public JpegGenerationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}