package com.poweredbypace.pace.exception;

public class FormulaParseException extends RuntimeException {

	private static final long serialVersionUID = 432276218787076247L;

	public FormulaParseException() {
		super();
	}

	public FormulaParseException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FormulaParseException(String arg0) {
		super(arg0);
	}

	public FormulaParseException(Throwable arg0) {
		super(arg0);
	}

}
