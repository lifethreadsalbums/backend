package com.poweredbypace.pace.domain;

public class WeightRule {
	
	private String conditionExpression;
	private String weightExpression;
	
	public String getConditionExpression() {
		return conditionExpression;
	}
	public void setConditionExpression(String conditionExpression) {
		this.conditionExpression = conditionExpression;
	}
	public String getWeightExpression() {
		return weightExpression;
	}
	public void setWeightExpression(String weightExpression) {
		this.weightExpression = weightExpression;
	}
	
}
