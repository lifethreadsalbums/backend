package com.poweredbypace.pace.domain.layout;

public class ProoferStats {
	
	private int numPending;
	private int numCompleted;
	private int numArchived;
	
	public int getNumPending() {
		return numPending;
	}
	public void setNumPending(int numPending) {
		this.numPending = numPending;
	}
	public int getNumCompleted() {
		return numCompleted;
	}
	public void setNumCompleted(int numCompleted) {
		this.numCompleted = numCompleted;
	}
	public int getNumArchived() {
		return numArchived;
	}
	public void setNumArchived(int numArchived) {
		this.numArchived = numArchived;
	}
	
	
}
