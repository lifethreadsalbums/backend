package com.poweredbypace.pace.exception;

public class EmailNotFoundException extends Exception {

	private static final long serialVersionUID = 5509378650704912262L;

	public EmailNotFoundException() {
		super();
	}

	public EmailNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public EmailNotFoundException(String arg0) {
		super(arg0);
	}

	public EmailNotFoundException(Throwable arg0) {
		super(arg0);
	}

	
}
