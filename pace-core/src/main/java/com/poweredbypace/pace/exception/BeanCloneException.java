package com.poweredbypace.pace.exception;

public class BeanCloneException extends RuntimeException {

	private static final long serialVersionUID = -3070693774335605820L;

	public BeanCloneException() {
		super();
	}

	public BeanCloneException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public BeanCloneException(String arg0) {
		super(arg0);
	}

	public BeanCloneException(Throwable arg0) {
		super(arg0);
	}

	
}
