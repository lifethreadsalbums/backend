package com.poweredbypace.pace.exception;

public class LinkExpiredException extends Exception {

	private static final long serialVersionUID = -1461444320056094148L;

	public LinkExpiredException() {
		super();
	}

	public LinkExpiredException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LinkExpiredException(String arg0) {
		super(arg0);
	}

	public LinkExpiredException(Throwable arg0) {
		super(arg0);
	}
	
	

}
